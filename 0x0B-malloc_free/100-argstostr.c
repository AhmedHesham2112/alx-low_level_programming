/**
 * argstostr - concatenates all the arguments of your program.
 * @ac: no of arguments.
 * @av: value of arguments.
 * Return: a pointer to a new string, or NULL if it fails
 */

char *argstostr(int ac, char **av)
{
	int i = ac;

	i++;
	return (*av);
}
